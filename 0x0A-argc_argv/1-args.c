#include <stdio.h>

/**
 * main - entry point
 * Description: program that prints the number
 * of arguments passed into it
 * @argc: count
 * @argv: array
 * Return: Always return 0
 */

int main(int argc, char *argv[] __attribute__((unused)))
{
	if (args > 0)
		printf("%d\n", argc - 1);
	return (0);
}
